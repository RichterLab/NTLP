def generate_name():
    """
    Generates a random name comprised of one adjective and one noun, separated
    by hyphens.

    Takes no arguments.

    Returns 1 value:

      generated_name - The randomly generated name.

    """

    number_adjectives = len( ADJECTIVES_LIST )
    number_nouns      = len( NOUNS_LIST )

    adjective_index = random.randint( 0, number_adjectives - 1 )
    noun_index      = random.randint( 0, number_nouns - 1 )

    return "{:s}-{:s}".format(
        ADJECTIVES_LIST[adjective_index],
        NOUNS_LIST[noun_index] )
